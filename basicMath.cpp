#include <cmath>
#include <iostream>

int main() {
    std::cout << "9+2=" << (9 + 2) << std::endl;
    std::cout << "7-2=" << (7 - 2) << std::endl;
    std::cout << "4/7=" << (4 / 2) << std::endl;
    std::cout << "4+4/2=" << (4 + 4 / 2) << std::endl;
    std::cout << "3+4*2=" << (3 + 4 * 2) << std::endl;
    // This function has a separate library, power/exponent
    std::cout << "5+pow(2,3)=" << (5 + pow(2, 3)) << std::endl;
    std::cout << "5.5/2=" << (5.5 / 2) << std::endl;
    std::cout << "Square root of 9=" << (sqrt(9)) << std::endl;
}
